  languages_by_style.each do |type, languages|
  	languages.each do |t, intcomp|
  		intcomp.each do |x, y|
  			new_hash[t] = {x => y, :style => [type]}
  		end
  	end
  end
  new_hash[:javascript][:style] << :oo
  return new_hash
end

